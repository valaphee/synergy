    J(0x0D, 0x4A),
    K(0x0E, 0x4B),
    L(0x0F, 0x4C),
    M(0x10, 0x4D),
    N(0x11, 0x4E),
    O(0x12, 0x4F),
    P(0x13, 0x50),
    Q(0x14, 0x51),
    R(0x15, 0x52),
    S(0x16, 0x53),
    T(0x17, 0x54),
    U(0x18, 0x55),
